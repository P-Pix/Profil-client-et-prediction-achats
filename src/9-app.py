# # Streamlit App – Recommandation personnalisée

# Cette application permet de tester les recommandations générées pour un client donné.

# Fonctionnalités :
# - Saisie de l’ID client
# - Affichage de son profil (segment RFM, derniers achats)
# - Affichage des recommandations générées (collaboratif + contenu)
# - Visualisation simple via Streamlit

# Objectif : illustrer de manière interactive le fonctionnement du moteur de recommandation.
